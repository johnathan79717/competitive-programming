void solve() {
    LL N, K;
    R(N, K);
    VI ps;
    vector<pair<PII, int>> segs;
    REP(i, N) {
        int x, y, v;
        R(x, y, v);
        ps.PB(x);
        ps.PB(y);
        segs.PB(MP(x, y), v);
    }
    SORT_UNIQUE(ps);
    N = ps.size();
    vector<VPII> s(N);
    for (auto &pr : segs) {
        int x = GET_POS(ps, pr.F.F), y = GET_POS(ps, pr.F.S), v = pr.S;
        s[y].PB(x, v);
    }

    atcoder::lazy_segtree<S, op, e, F, mapping, composition, id> seg(N);
    LL ans = 0;
    REP(r, N) {
        seg.set(r, ps[r] * K);
        for (auto &pr : s[r]) {
            seg.apply(0, pr.F + 1, pr.S);
        }
        ans = max(ans, seg.all_prod() - ps[r] * K);
    }
    W(ans);
}

