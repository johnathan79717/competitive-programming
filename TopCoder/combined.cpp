class SubmultiplesOfN {
public:
    int count(string B, int N) {
	int M = SZ(B);
	vector<vector<mint>> dp(M+1, vector<mint>(N));
	VI prev(10, -1);
	dp[0][0] = 1;
	prev[0] = 0;
	REP(j, SZ(B)) {
	    int b = B[j] - '0';
	    REP(i, N) {
		dp[j+1][i] += dp[j][i];
		dp[j+1][(i * 10 + b) % N] += dp[j][i] - (prev[b] < 0 ? 0 : dp[prev[b]][i]);
	    }
	    prev[b] = j;
	}
	return (dp[M][0] - 1).val();
    }
};

