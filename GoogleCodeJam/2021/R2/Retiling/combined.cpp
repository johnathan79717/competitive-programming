void solve() {
    int r, c, F, S;
    R(r, c, F, S);
    atcoder::mf_graph<int> g(r * c + 2);
    int s = r * c, t = r * c + 1;
    REP(i, r) {
        R(current[i]);
    }
    int ans = 0;
    REP(i, r) {
        R(target[i]);
        REP(j, c) {
            if (current[i][j] != target[i][j]) {
                ans++;
                if ((i + j) % 2) {
                    g.add_edge(s, i * c + j, 1);
                } else {
                    g.add_edge(i * c + j, t, 1);
                }
                if (j && current[i][j-1] != target[i][j-1] && target[i][j] != target[i][j-1]) {
                    if ((i + j) % 2) {
                        g.add_edge(i * c + j, i * c + j - 1, 1);
                    } else {
                        g.add_edge(i * c + j - 1, i * c + j, 1);
                    }
                }
                if (i && current[i-1][j] != target[i-1][j] && target[i-1][j] != target[i][j]) {
                    if ((i + j) % 2) {
                        g.add_edge(i * c + j, (i-1) * c + j, 1);
                    } else {
                        g.add_edge((i-1) * c + j, i * c + j, 1);
                    }
                }
            }
        }
    }
    W(ans - g.flow(s, t));
}

