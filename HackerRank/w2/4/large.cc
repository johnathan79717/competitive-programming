struct Info
{
  bool fail;
  LL m, M, d;
};

Info operator+(Info const &x1, Info const &x2) {
  Info x;
  x.fail = x1.fail || x2.fail || x1.M + x1.d < x2.m;
  x.m = max(x1.m, x2.m-x1.d);
  x.d = x1.d + x2.d;
  x.M = max(x1.m, min(x2.M-x1.d, x1.M));
  return x;
}

