const int MOD = 99991;
struct Z {
  int i;
  Z():i(0) {}
  Z(int i): i(i) {}
  void operator +=(const Z& z) { i += z.i; if(i >= MOD) i -= MOD; }
  void operator -=(const Z& z) { i -= z.i; if(i < 0) i += MOD; }
  void operator *=(const Z& z) { i = (LL) i * z.i % MOD; }
  void operator /=(const Z& z) { (*this) *= z.inverse(); }
  Z operator +(const Z& z) const { Z ret(i); ret += z; return ret; }
  Z operator -(const Z& z) const { Z ret(i); ret -= z; return ret; }
  Z operator *(const Z& z) const { Z ret(i); ret *= z; return ret; }
  Z operator /(const Z& z) const { return (*this) * z.inverse(); }
  // Z operator -() const { return Z(-i); }
  Z inverse() const {
    int a = i, d = MOD, x = 0, s = 1;
    while(a) {
      int q = d / a, r = d % a, t = x - q * s;
      d = a, a = r, x = s, s = t;
    }
    if (d != 1) return -1;
    return x < 0 ? x + MOD : x;
  }
  Z pow(int b) {
    Z x=1,y=*this; // ll is taken to avoid overflow of intermediate results
    while(b > 0){
      if(b%2 == 1)
        x *= y;
      y *= y; // squaring the base
      b /= 2;
    }
    return x;
  }
};

Z fact[MOD];

pair<Z, int> mod_fact(LL n) {
  if(n == 0) return MP(1, 0);
  pair<Z, int> pr = mod_fact(n / MOD);
  if(n / MOD % 2) return MP(pr.first * (Z(0) - fact[n % MOD]), pr.second + n / MOD);
  return MP(pr.first * fact[n % MOD], pr.second + n / MOD);
}

Z mod_comb(LL n, LL k) {
  if(n < 0 || k < 0 || n < k) return 0;
  auto p1 = mod_fact(n), p2 = mod_fact(k), p3 = mod_fact(n-k);
  if(p1.second > p2.second + p3.second) return 0;
  return p1.first / (p2.first * p3.first);
}

